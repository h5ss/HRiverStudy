package HR_assignment2;

public class Loop9053_ex07 {
    public static void main(String[] args) {
        for (int i=1;i<6;i++){
            for(int j=0;j<5;j++){
                System.out.printf(i+" ");
            }
            System.out.println();
        }
        System.out.println();
        for (int i=1;i<6;i++){
            for(int j=1;j<6;j++){
                System.out.printf(j+" ");
            }
            System.out.println();
        }
    }
}
